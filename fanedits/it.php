		<section class="purpose" id="purpose">
			<h2 class="sub-title">The Purpose of This Edit</h2>
			<p>I've always loved the 1990 "IT" miniseries. I taped it off TV when it originally aired and it stayed in my collection for years; until the tape wore out. Now I have a Digital HD copy. The director's commentary is wonderful, by the way. So, it is announced that the new "IT" re-adaptation is coming out in September of 2017. Of course, I'm super excited to see what this will look like with modern effects and budgets. My take? It was fine. Without Stephen King's insane, coked-out, sex-pervert novel as the source material and especially without the iconic miniseries and work of Tim Curry (and -- I would argue -- Jonathan Brandis), I doubt folks turn out in droves for it after the string of poor King adaptations recently (Cell, Carrie, Dark Tower, etc).</p>
			<p>But that's not why I decided to take on the self-imposed challenge of polishing the original miniseries, it was because people starting coming out of the woodwork to talk about how bad the original miniseries was. I mean, the new movie was good -- I bought it, and will watch it every couple years -- but I think from a narrative point-of-view it falls well short of the original. And, in my considered opinion, not a single one of those kids is better than Jonathan Brandis (or the version of Bill Denbrough written for him).</p>
			<p>So I set out to update the existing miniseries and turn it into a single feature without a segmented narrative. Minimize the downtime, and trim back wherever possible. I believe I have succeeded at tightening up the content, masking the special effects that did not hold up very well, and presenting the best possible version of the ORIGINAL content.</p>
			<p>Please, enjoy.</p>
		</section>
		<section class="edits" id="edits">
			<h2 class="sub-title">List of Major Edits</h2>
			<p>What follows is a list of all the major, noticable edits in the film. Every frame was tweaked or updated in some way.</p>
			<ul>
				<li>New opening credits.</li>
				<li>Many new musical cues and sound effects throughout.</li>
				<li>Colour-graded the entire film.</li>
				<li>Cropped to 16:9 (and lovingly pan-and-scanned throughout)</li>
				<li>Removed Mike Hanlon’s voiceover wherever it occurs.</li>
				<li>New establishing plates for all cities outside Derry.</li>
				<li>Bill &amp; Audra’s establishing scene is shortened.</li>
				<li>“Georgie &amp; Pennywise” tightened up.</li>
				<li>Ben’s awkward dialog with his date is removed.</li>
				<li>Ben’s first encounter with IT tightened.</li>
				<li>Bev &amp; Tom meeting the Japenese businessmen removed.</li>
				<li>Bev’s “Call” removed.</li>
				<li>Tom’s endless search for champaign is VERY tightened.</li>
				<li>Moved Eddie’s ‘revelation’ from Mr. Keene to immediately after his adult character is established. This makes the payoff later more satisfying.</li>
				<li>Theater (I Was A Teenage Werewolf) scene tightened.</li>
				<li>Removed awkward “men or mice” line.</li>
				<li>Eddie’s shower scene re-cut to heighten tension, add franticness, and hide poor visual effects.</li>
				<li>Removed Eddie’s chaufer from the train scene. I mean… ugh.</li>
				<li>Adult Richie’s establishing shot scaled WAY back because it adds nothing.</li>
				<li>Removed Richie’s entire conversation with his agent. It was awful.</li>
				<li>Werewolf scene re-paced.</li>
				<li>Removed intervention of shop owner from the Hanlon/Bowers scene. Doesn’t make sense that he would step in when it is later established that adults don’t get involved.</li>
				<li>Rock fight scene got new foley, and dialog cuts.</li>
				<li>The daylight-is-fading-and-we-need-to-get-this-in-one-shot super-pan of the now complete Loser’s Club changed to standard cuts.</li>
				<li>Bill’s “Help Me” speech now 40% more inspirational!</li>
				<li>Adult Stan’s “sex maniac” dialog and lingering shots of Perfect Strangers is cut.</li>
				<li>Stan’s Mummy encouter added during the phone call with Mike.</li>
				<li>Vic Criss’ encounter with the Deadlights tightened</li>
				<li>Deadlights have a custom lighting effect</li>
				<li>Henry whitehair is cut</li>
				<li>Eddie’s “Battery Acid” is re-timed to make his intervention for Stan seem more “last-minute”</li>
				<li>The “Drain Incident” has been quick-cut to mask the poor effects somewhat without losing the sense of place or understanding what is happening.</li>
				<li>“Stan Found” has been recut for gradual discovery and to remove the total lack of blood in the opening shot.</li>
				<li>“Audra/Greco” moved to right after “Stan Found”, but Greco is removed completely, it doesn’t set up or pay off anything, so it’s cut.</li>
				<li>The various “[Character] Arrives in Derry” scenes have been reordered.</li>
				<li>The “Bill &amp; Mike Ride Silver” montage was cut because, honestly, it feels like filler.</li>
				<li>“Ben Helps Fellow Fat Kid” is cut because it was too convenient, and the new fat kid cannot act. At all.</li>
				<li>“Bev Returns Home”: removed close-up of “MARSH” doorbell plate, because it was too obvious. Now it’s more subtle, more of a detail that will reward repeat viewings.</li>
				<li>“Bev Returns Home”: removed the “You’re not real!” exchange by the closed door, makes it feel more like an escape and less like IT just allowed Bev to leave.</li>
				<li>Re-ordered the Library scene between Richie and Pennywise</li>
				<li>Trimmed down the “Dinner Montage” at the Chinese restaurant.</li>
				<li>Removed the image of Pennywise superimposed on the moon during “Wake Up, Henry”. It is still implied that he sees Pennywise in the moon, but not shown directly.</li>
				<li>Trimmed the “Fortune Cookie” sequence to tighten it up a bit and mask some of the lesser effects.</li>
				<li>Cut A LOT of Harry Anderson’s CLEARLY improvised comedy during the “Calling Stan” sequence.</li>
				<li>Re-cut Bill’s re-telling of Stan’s “Mummy Encounter” so we ONLY see flashbacks of things that Bill witnessed firsthand.</li>
				<li>Trimmed the part where Mike slams the prop fridge and it pops back open momentarily. That has bugged me since the 90s.</li>
				<li>Trimmed the Clown Dog from “Henry Escapes” and leaves the how nebulous, but implies that he used the switchblade given to him by It-as-Belch.</li>
				<li>Removed some of the hokey dialog from “Ben and Bev Kiss”, but because of the single tracking shot (there are a LOT of those in this miniseries) I couldn’t really do much with the “Why is it so mean?” mini-monologue.</li>
				<li>Bill’s “I don’t want to be scared anymore” speech is now 30% more inspirational!</li>
				<li>Re-cut the “Pennywise in the vines” monologue to hide the superimposed pennywise. We still see the Losers Club looking at a singular location, but what they actually <em>see</em> is not show to the viewer.</li>
				<li>HEAVILY re-cut the final fight with IT in it’s spider form. Removed almost all wide shots of the spider. Removed MOST of the close-ups. Basically tried to show as little of it as possible. While I think the practical spider looks pretty good, I think it’s better to be less explicit, and allow each viewer to fill in the blanks on their own. Let the creature be more personal that way.</li>
				<li>The film now ends with the remaining Losers Club members standing outside the cave. No closure, or happy ending. They survive. That’s all.</li>
				<li>Custom end credits (expanded from original, using IMDB to credit many more folks who went unrecognized in the original miniseries).</li>
			</ul>
