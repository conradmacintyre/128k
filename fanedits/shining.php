			<h2 class="sub-title">List of Major Edits</h2>
			<p>I actually ended up removing 2 hours and 13 minutes from the runtime. This is in spite of adding back more than 7 minutes worth of deleted material. This is not an exhaustive cut list. These are just the highlights. There are a bunch of trims and dialogue cuts/replacements that are not listed. Hopefully all of those are transparent.</p>
			<ul>
				<li>!* THE CGI BUSHES ARE GONE *!
				<li>Entirely new intro sequence with incorporated (minimal) titles.</li>
				<li>New music cues throughout by David Fesliyan.</li>
				<li>Re-purposed various establishing and cutaway shots throughout.</li>
				<li>Removed fade-to-black commercial breaks wherever they appear.</li>
				<li>Cut many, many insert shots of the boiler. You're a grown-up. You'll remember.</li>
				<li>Cut Jack & Watson talking endlessly about the boiler in the basement.</li>
				<li>Cut Jack & Ullman outside the Overlook.</li>
				<li>Cut Danny & Wendy inside the apartment.</li>
				<li>The Jack & Danny incident has been moved to later in the film.</li>
				<li>Jack & Wendy's post-incident conversation has been cut.</li>
				<li>Wendy searching Jack's office has been cut.</li>
				<li>Removed all physical manifestations of Tony wherever they appear.</li>
				<li>Jack's AA call outside the bar has been cut.</li>
				<li>The drive to Colorado has been re-foley'd, re-scored, and heavily trimmed.</li>
				<li>Arriving at the Overlook has been heavily trimmed.</li>
				<li>Replacing a cheesy jump-scare with something tied to the plot.</li>
				<li>Trimmed the Torrances meeting/touring with Halloran & Watson quite heavily.</li>
				<li>Trimmed Danny & Dick quite a bit.</li>
				<li>Trimmed Jack & the bees.</li>
				<li>Trimmed the "Back with the Groceries" scene. A lot.</li>
				<li>Shortened Danny & Jack's conversation.</li>
				<li>Moved long panning shot of the Overlook coming to life to later in the film.</li>
				<li>Re-cut Jack & the bees part 2.</li>
				<li>Cut the AA meeting.</li>
				<li>Added the Jack and Danny incident as a flashback while Wendy is with the doctor.</li>
				<li>Sliced-up Jack in the basement to use over various days instead of all at once.</li>
				<li>Moved Danny & Wendy playing in the snow to after Jack in the basement.</li>
				<li>Moved Danny checking out Room 217 to after the snow.</li>
				<li>Re-edited Jack confronting Danny so that Jack seems less crazy at this early point in the story.</li>
				<li>Added deleted scene of Jack and Danny being playful together.</li>
				<li>Moved scene of Jack and the Topiary up to now.</li>
				<li>Danny & Wendy talk about Jack trimmed, some dialog replacement.</li>
				<li>Jack & Wendy in the lobby trimmed a bunch.</li>
				<li>Re-cut Danny in 217 and Jack & his father to be a bit more interactive. The two events come off as more connected rather than simply simultaneous... hopefully.</li>
				<li>Removed the close-up on the 217 Woman.</li>
				<li>Removed Jack explicitly destroying the radio.</li>
				<li>Moved Jack "discovering" the broken radio to AFTER finding Danny on the stairs.</li>
				<li>Trimmed the scene where the Torrances try to sort out what happened to Danny.</li>
				<li>Trimmed the exposition scene in the kitchen.</li>
				<li>Moved that panning shot of the Overlook coming to lift to hear, under Danny's voiceover.</li>
				<li>Tightened Jack exploring 217.</li>
				<li>Tightened Jack & Wendy fighting.</li>
				<li>Added deleted scene of Jack explaining his theory of events.</li>
				<li>Trimmed Jack and the snowmobile to keep a little bit of mystery, or at least make it less obvious.</li>
				<li>Trimmed Jack and Wendy and the snowmobile.</li>
				<li>Heavily cut the hedge attack on Danny. Made it possibly just in Wendy's mind???</li>
				<li>Replaced Danny messing with the clock in the ballroom with an earlier scene to lead into the vision.</li>
				<li>Removed all instances of Dick Halloran on his way to the Overlook. We have no idea if he's coming or not.</li>
				<li>Cut Danny and the Dogman.</li>
				<li>Cut all the Overlook scares against Jack. Seems the hotel would be trying to court Jack, not scare him.</li>
				<li>Cut a crap-ton of the ballroom stuff. Including the excessive shot of King's cameo.</li>
				<li>Cut shots of Jack in the empty ballroom. Not clear if it's in his mind or a real manifestation.</li>
				<li>Trimmed Wendy's route to find Jack so it's a little more direct. Not that she would know, but since we DO know, it reduces the tension.</li>
				<li>Trimmed the fight between Jack & Wendy.</li>
				<li>Trimmed dragging Jack to the pantry.</li>
				<li>Cut Wendy and Jack talking through the door.</li>
