		<section class="purpose" id="purpose">
			<h2 class="sub-title">The Purpose of This Edit</h2>
			<p>I was so happy with how my It (1990) miniseries edit came out that I wanted to tackle some other Stephen King minis from the 90s. There are lots of great (and some poor) options to choose from; The Stand, The Langoliers, Storm of the Century, Tommyknockers, and the Golden Years. But one that I feel is criminally underrated is the 1997 re-adaptation of The Shining. King was infamously unhappy with Kubrick's 1980 film, and sought to fix several issues he saw with it:</p>
			<p>Firstly, Nicholson's Jack Torrance starts off as an asshole, and finishes off as a <strong>giant</strong> asshole whereas Weber's take on the character is a loving — but screwed up — husband and father whose weaknesses make him an easy target for the malevolence of the hotel.</p>
			<p>Secondly, Duvall's Wendy Torrance is what King described as "a screaming dishrag". A cowardly, mousey character. Really the only kind of woman who would stay with that version of Jack. De Mornay's Wendy is a distrustful — but trying — mother, and a strong woman who is trying to help her husband get through the valley.</p>
			<p>Thirdly, there are a few smaller issues which only serve to bring the story more in-line with the book. This includes the fate of Halloran, the topiary (rather than the maze), the croquet mallet (rather than the axe), and the climax. King also quipped of the two adaptations of the book that he was warm and Kubrick was cold.</p>
			<p>To that end, what I hoped to accomplish with this edit was to cut this down to a reasonable feature-length runtime, remove all the bad CGI, some of the poorer line deliveries, the excessive amount of expositionary dialogue, and tighten up the pacing. I also re-worked the ending, and re-structured the plot so that it's not immediately clear how it's going to end OR who the hotel is after.</p>
			<p>Please, enjoy.</p>
