						<li>Stu&#8217;s Struggle</li>
						<li>The End of Vegas</li>
					</ul>
				</li>
				<li>Cut the Project Blue outbreak scene.</li>
				<li>Cut the Fly Buddy commercial.</li>
				<li>Cut the Police Check-in.</li>
				<li>Cut the first meeting with Larry.</li>
				<li>Cut Vic&#8217;s death.</li>
