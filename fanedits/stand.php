			<h2 class="sub-title">List of Major Edits</h2>
			<p>What follows is a list of all the major, noticable edits in the film. Every frame was tweaked or updated in some way.</p>
			<ul>
				<li>All-new opening sequence.</li>
				<li>26 new pop/rock songs featured throughout.</li>
				<li>Colour-graded many scenes and sequences.</li>
				<li>5 new composite shots</li>
				<li>Cropped to 16:9 (and lovingly pan-and-scanned throughout)</li>
				<li>I broke the miniseries down into 131 unique sequences. 91 of were not removed entirely, of that group, only the following were not trimmed or tightened in some way:
					<ul>
						<li>Nadine&#8217;s Gone</li>
						<li>Mother Abigail&#8217;s Prayer of Repentance</li>
