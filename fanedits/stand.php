		<section class="purpose" id="purpose">
			<h2 class="sub-title">The Purpose of This Edit</h2>
			<p>The Stand is a fantastic miniseries. And often unfairly maligned because of the poor visual effects (true) and terribly literal "Hand of God" finale (also true). Despite it's flaws, I have enjoyed this behemoth every time I have watched it. But one thing I could rarely do? Watch it all in a single day. And certianly never in a single night. It runs around 6 hours and 40 minutes.</p>
			<p>My goal was simple: Cut back the miniseries into a single-sitting feature-length film. I decided that the final product could be no longer than The Godfather Part II, which clocks-in at 3 hours and 21 minutes. I originally did this edit with a DVD source and it came out okay-ish, but there were both narrative and quality problems. Once I heard that there would be a BluRay release I got very excited to try again.</p>
			<p>This Cinematic Cut of The Stand clocks in at a comparatively svelte 3 hours and 5 minutes. And also makes heavy use of popular music for it's soundtrack. Everything from Procol Harem to The Police to Pink Floyd. I think this came out very well and I am excited to share it with you.</p>
			<p>Please enjoy, and your feedback is very welcome.</p>
		</section>
